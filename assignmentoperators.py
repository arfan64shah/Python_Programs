#lets declare a variable named as x

#simple assignment
x = 30

#Add and equal operator
x += 3
print(x)

#Subtract and equal
x -= 3
print(x)

#multiply and equal
x *= 3
print(x)

#divide and equal
x /= 3
print(x)

#modulus and equal
x %= 4
print(x)

#floor division and equal
x //= 3
print(x)

#exponent and equal
x **= 3
print(x)

#bitwise AND
x &= 3
print(x)

#bitwise OR
x |= 3
print(x)

#bitwise XOR
x ^= 3
print(x)

#bitwise right shift
x >>= 3
print(x)

#bitwise left shift
x <<= 3
print(x)


