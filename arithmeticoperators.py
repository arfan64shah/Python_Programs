#declare two numbers
num1 = 5
num2 = 6

#addition operator
sum = num1 + num2
print("The sum is ", sum)

#subtraction operator
sub = num1 - num2
print("The difference is ", sub)

#multiplication operator
product = num1 * num2
print("The product is ", product)

#division operator
division = num1 / num2
print("The division is ", division)

#modulus operator
mod = num1 % num2
print("Modulus of given numbers is ", mod)

#exponent operator
exp = num1 ** num2
print("Answer after exponentiation is ", exp)

#floor division
floorDiv = num1 // num2
print("Floor division is ", floorDiv)